<h4>4.你追求什么样的生活方式？</h4>

<p>关于这点，昨晚和朋友正好聊起。如今正值找工作的时候，他为我担心——他觉得我一点目标都没有，不像他，他给自己定了目标，要做到处长级别以上。（他家里有关系有背景，不犯大错误，时间到了就可以实现了），然后我说，我也是有目标的啊，我想做餐饮业。然后他超级不屑的说：这算什么目标？

呵呵。这就是我追求的生活方式吧，不给自己定下高度，而只是给自己找到一条想走的路。能到多远，我不强求，我只是希望自己做自己爱做的事，而不用为了达到一个目标而去做些自己不喜欢的事，那样的目标，宁可不要。是虚空，是捕风。</p>

